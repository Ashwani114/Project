# Task 1: Basic Arithmetic Operations
first_number = int(input("Enter the first number: "))  # Get first number from user and convert to integer
second_number = int(input("Enter the second number: "))  # Get second number from user and convert to integer

add = first_number + second_number        # Calculate addition of the two numbers
subtract = first_number - second_number   # Calculate subtraction of the two numbers
multiply = first_number * second_number   # Calculate multiplication of the two numbers
divide = first_number / second_number     # Calculate division of the two numbers

print(f"Addition: {add}")                 # Print the result of addition
print(f"Subtraction: {subtract}")         # Print the result of subtraction
print(f"Multiplication: {multiply}")      # Print the result of multiplication
print(f"Division: {divide}")              # Print the result of division